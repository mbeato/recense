/**
 * serve-cli — HTTP serving mode adapter (Phase 12, SERVE-01/SERVE-02).
 *
 * Entry point: `recense serve --db <path>` (dispatched via spawnScript from brain.ts).
 *
 * Exposes two surface protocols on one port (D-01):
 *   REST  — POST /v1/search|add|ask with Phase 11 response shapes (D-04)
 *   MCP   — POST /mcp (stateless MCP-over-HTTP, D-03)
 *   Health — GET /health (unauthenticated, D-13)
 *
 * Auth (SERVE-02): Bearer token required for every path except /health. Token is
 * born on first `recense serve` run (crypto.randomBytes(32)), stored in chmod-600
 * sleep.env as RECENSE_SERVE_TOKEN, printed once, never again (D-05/D-06/D-07).
 *
 * Security invariants:
 *   T-12-03: auth gate fires BEFORE any body parse; identical 401 for absent vs wrong token.
 *   T-12-04: crypto.timingSafeEqual for constant-time comparison; length pre-check guards throw.
 *   T-12-05: token lives only in chmod-600 sleep.env; never in logs or error bodies.
 *   T-12-06: safe-null discipline — raw errors caught, replaced with { error: 'internal_error' }.
 *   T-12-07: 64KB body cap (incremental byte counting, not content-length); requestTimeout=30s.
 *   T-12-08: default bind 127.0.0.1; --host 0.0.0.0 requires explicit opt-in (D-09).
 *   T-12-09: sessionIdGenerator: undefined (stateless); GET /mcp → 405.
 */
import { appendFileSync } from 'fs';
import * as http from 'node:http';
import { timingSafeEqual, randomBytes } from 'node:crypto';
import { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';
