function jsonOk(res: http.ServerResponse, body: unknown): void {
  const payload = JSON.stringify(body);
  res.writeHead(200, { 'content-type': 'application/json' });
  res.end(payload);
}

function jsonError(res: http.ServerResponse, status: number, body: unknown): void {
  const payload = JSON.stringify(body);
  res.writeHead(status, { 'content-type': 'application/json' });
  res.end(payload);
}

// ---------------------------------------------------------------------------
// buildMcpServer — per-request McpServer construction (WR-03)
// ---------------------------------------------------------------------------

/**
 * Construct a fresh McpServer with the three memory tools registered.
 *
 * WR-03: the stateless StreamableHTTPServerTransport tracks in-flight
 * request→response mappings keyed by JSON-RPC request id, so the SDK requires a
 * NEW server + transport per request (see the SDK's simpleStatelessStreamableHttp
 * example) — a shared instance misroutes/drops responses when concurrent clients
 * reuse ids (two independent clients both starting at id 1 is the normal case).
 * The engine/ops stay shared (D-02); only this cheap MCP wrapper is per-request.
 */
function buildMcpServer(ops: MemoryOps): McpServer {
  const mcpServer = new McpServer(
    { name: 'recense', version: '0.1.0' },
    {
      instructions:
        'recense tools: memory_search (LLM-free semantic retrieval), memory_add ' +
        '(record a fact/observation), memory_ask (question answering over memory). ' +
        'Writes land as episodes; abstraction/consolidation runs in the hourly sleep pass, not inline.',
    },
  );
  // Register the same three tools as the stdio MCP surface (same ops → one engine instance, D-02).
  registerMemoryTools(mcpServer, ops);
  return mcpServer;
}

// ---------------------------------------------------------------------------
// createBrainHttpServer — factory (D-01/D-02)
// ---------------------------------------------------------------------------

/**
 * Build the brain HTTP server: wire the shared memory-ops core, construct the MCP
 * server + stateless transport, and return an http.Server that is NOT yet listening.
 * Callers (CLI entry and tests) call server.listen() themselves.
 *
 * Design: factory-exported so tests can inject token + MockModelProvider without
 * any subprocess or env-file access (same pattern as createBrainMcpServer).
 */
export async function createBrainHttpServer(
  opts: BrainHttpServerOptions,
): Promise<BrainHttpServer> {
  // ── 1. Wire shared memory-ops core (D-02: one engine, one source tag 'http') ──
  // separateReadHandle: true → search path uses its own read-only DB handle (criterion 4).
  const { ops, close: closeEngine } = await wireMemoryEngine({
    dbPath: opts.dbPath,
    provider: opts.provider,
    source: 'http',
    separateReadHandle: true,
  });

  // ── 2. MCP server + transport are created PER REQUEST in the /mcp handler (WR-03) ──

  // ── 3. Read package.json version for /health (fallback to '0.1.0') ─────────
  let pkgVersion = '0.1.0';
  try {
    // eslint-disable-next-line @typescript-eslint/no-var-requires
    pkgVersion = (require('../../package.json') as { version: string }).version ?? '0.1.0';
  } catch { /* fallback */ }

  // ── 4. Request handler ──────────────────────────────────────────────────────
  const requestHandler = async (req: http.IncomingMessage, res: http.ServerResponse): Promise<void> => {
    const start = Date.now();
    const url = (req.url ?? '/').split('?')[0]!;

    // ── /health — unauthenticated (D-13); MUST appear before the auth gate ──
    if (url === '/health' && req.method === 'GET') {
      jsonOk(res, { status: 'ok', version: pkgVersion });
      logRequest('GET', '/health', 200, Date.now() - start);
      return;
    }

    // ── Auth gate — fires for ALL other paths BEFORE any body read (T-12-03) ──
    if (!checkAuth(req, opts.token)) {
      jsonError(res, 401, { error: 'unauthorized' });
      logRequest(req.method ?? 'UNKNOWN', url, 401, Date.now() - start, true);
      return;
    }

    // ── Authenticated routes ────────────────────────────────────────────────

    // POST /v1/search — LLM-free read path (no lock; separate read handle via shared core)
    if (url === '/v1/search' && req.method === 'POST') {
      const rawBody = await readBody(req);
      if (rawBody === null) {
        jsonError(res, 413, { error: 'payload_too_large' });
        logRequest('POST', url, 413, Date.now() - start);
        return;
      }
      let parsed: { query?: unknown };
      try { parsed = JSON.parse(rawBody) as { query?: unknown }; } catch {
        jsonError(res, 400, { error: 'bad_request', detail: 'invalid json' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      if (typeof parsed.query !== 'string') {
        jsonError(res, 400, { error: 'bad_request', detail: 'missing required field: query' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      try {
        const results = await ops.search(parsed.query);
        jsonOk(res, { results });
        logRequest('POST', url, 200, Date.now() - start);
      } catch (err) {
        log(`/v1/search error: ${err}`);
        jsonError(res, 500, { error: 'internal_error' });
        logRequest('POST', url, 500, Date.now() - start);
      }
      return;
    }

    // POST /v1/add — episodic write; per-call lock (T-12-02)
    if (url === '/v1/add' && req.method === 'POST') {
      const rawBody = await readBody(req);
      if (rawBody === null) {
        jsonError(res, 413, { error: 'payload_too_large' });
        logRequest('POST', url, 413, Date.now() - start);
        return;
      }
      let parsed: { content?: unknown; origin?: unknown };
      try { parsed = JSON.parse(rawBody) as { content?: unknown; origin?: unknown }; } catch {
        jsonError(res, 400, { error: 'bad_request', detail: 'invalid json' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      if (typeof parsed.content !== 'string') {
        jsonError(res, 400, { error: 'bad_request', detail: 'missing required field: content' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      const origin = typeof parsed.origin === 'string' ? parsed.origin : undefined;
      try {
        const ack = await ops.add(parsed.content, origin);
        jsonOk(res, ack);
        logRequest('POST', url, 200, Date.now() - start);
      } catch (err) {
        if (err instanceof MemoryBusyError) {
          jsonError(res, 503, { error: 'service_unavailable', detail: 'memory busy; retry in a moment' });
          logRequest('POST', url, 503, Date.now() - start);
          return;
        }
        log(`/v1/add error: ${err}`);
        jsonError(res, 500, { error: 'internal_error' });
        logRequest('POST', url, 500, Date.now() - start);
      }
      return;
    }

    // POST /v1/ask — HybridResponder; per-call lock (T-12-02)
    if (url === '/v1/ask' && req.method === 'POST') {
      const rawBody = await readBody(req);
      if (rawBody === null) {
        jsonError(res, 413, { error: 'payload_too_large' });
        logRequest('POST', url, 413, Date.now() - start);
        return;
      }
      let parsed: { query?: unknown };
      try { parsed = JSON.parse(rawBody) as { query?: unknown }; } catch {
        jsonError(res, 400, { error: 'bad_request', detail: 'invalid json' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      if (typeof parsed.query !== 'string') {
        jsonError(res, 400, { error: 'bad_request', detail: 'missing required field: query' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      try {
        const out = await ops.ask(parsed.query);
        jsonOk(res, { answer: out.answer, origin: out.origin });
        logRequest('POST', url, 200, Date.now() - start);
      } catch (err) {
        if (err instanceof MemoryBusyError) {
          jsonError(res, 503, { error: 'service_unavailable', detail: 'memory busy; retry in a moment' });
          logRequest('POST', url, 503, Date.now() - start);
          return;
        }
        log(`/v1/ask error: ${err}`);
        jsonError(res, 500, { error: 'internal_error' });
        logRequest('POST', url, 500, Date.now() - start);
      }
      return;
    }

    // GET /v1/surface — LLM-free ranked surface items; lock-free read (D-95, T-12-02 N/A)
    if (url === '/v1/surface' && req.method === 'GET') {
      try {
        // url has query stripped — read params from req.url directly (serve-cli.ts 276).
        const sp = new URL(req.url ?? '/', 'http://x').searchParams;
        const surfaceOpts: { maxNonP0?: number; gracePeriodMs?: number } = {};
        // WR-03: `limit` tunes the D-09 non-P0 rolling cap (maxNonP0), NOT a total
        // response-size limit — P0 items always bypass the cap (surface-store.ts), so the
        // response is never bounded below the P0 count. Clamp to a sane ceiling so a caller
        // passing e.g. ?limit=1000000 cannot effectively disable the cap and pull every
        // lower-tier row.
        const limitRaw = sp.get('limit');
        if (limitRaw !== null) {
          const n = Number(limitRaw);
          if (Number.isFinite(n) && n > 0) surfaceOpts.maxNonP0 = Math.min(Math.floor(n), 50);
        }
        const graceRaw = sp.get('grace_hours');
        if (graceRaw !== null) {
          const h = Number(graceRaw);
          if (Number.isFinite(h) && h >= 0) surfaceOpts.gracePeriodMs = h * 60 * 60 * 1000;
        }
        const items = await ops.surface(Object.keys(surfaceOpts).length > 0 ? surfaceOpts : undefined);
        jsonOk(res, { items });
        logRequest('GET', url, 200, Date.now() - start);
      } catch (err) {
        log(`/v1/surface error: ${err}`);
        jsonError(res, 500, { error: 'internal_error' });
        logRequest('GET', url, 500, Date.now() - start);
      }
      return;
    }

    // POST /v1/surface/seen — record outcome; per-call lock (T-12-02)
    if (url === '/v1/surface/seen' && req.method === 'POST') {
      const rawBody = await readBody(req);
      if (rawBody === null) {
        jsonError(res, 413, { error: 'payload_too_large' });
        logRequest('POST', url, 413, Date.now() - start);
        return;
      }
      let parsed: { node_id?: unknown; occurrence_due_at?: unknown; outcome?: unknown; snooze_until?: unknown };
      try { parsed = JSON.parse(rawBody) as typeof parsed; } catch {
        jsonError(res, 400, { error: 'bad_request', detail: 'invalid json' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      // T-21-07: validate all fields before any write
      if (typeof parsed.node_id !== 'string' || typeof parsed.occurrence_due_at !== 'string') {
        jsonError(res, 400, { error: 'bad_request', detail: 'node_id and occurrence_due_at must be strings' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      if (Number.isNaN(Date.parse(parsed.occurrence_due_at))) {
        jsonError(res, 400, { error: 'bad_request', detail: 'occurrence_due_at is not a valid date' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      const VALID_OUTCOMES = ['surfaced', 'seen', 'snoozed', 'completed', 'dismissed'] as const;
      type ValidOutcome = typeof VALID_OUTCOMES[number];
      if (parsed.outcome !== undefined &&
          (typeof parsed.outcome !== 'string' || !VALID_OUTCOMES.includes(parsed.outcome as ValidOutcome))) {
        jsonError(res, 400, { error: 'bad_request', detail: 'outcome must be one of: surfaced, seen, snoozed, completed, dismissed' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      if (parsed.snooze_until !== undefined && parsed.snooze_until !== null &&
          (typeof parsed.snooze_until !== 'string' || Number.isNaN(Date.parse(parsed.snooze_until)))) {
        jsonError(res, 400, { error: 'bad_request', detail: 'snooze_until is not a valid date' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      // WR-01: enforce the documented contract (SurfaceSeenParams.snooze_until: "required
      // when outcome = 'snoozed'"). Without a future snooze_until, isExcluded() takes the
      // `null !== null → false` branch and the item is never excluded — a silent no-op snooze
      // that keeps re-surfacing (repeated unwanted Phase-22 push notifications). Fail fast.
      if (parsed.outcome === 'snoozed' &&
          (typeof parsed.snooze_until !== 'string' || Number.isNaN(Date.parse(parsed.snooze_until)))) {
        jsonError(res, 400, { error: 'bad_request', detail: "snooze_until is required when outcome='snoozed'" });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      const snoozeUntil = parsed.snooze_until === null ? null :
        typeof parsed.snooze_until === 'string' ? parsed.snooze_until :
        undefined;
      try {
        const ack = await ops.surfaceSeen({
          node_id:           parsed.node_id,
          occurrence_due_at: parsed.occurrence_due_at,
          outcome:           parsed.outcome as ValidOutcome | undefined,
          snooze_until:      snoozeUntil,
        });
        jsonOk(res, ack);
        logRequest('POST', url, 200, Date.now() - start);
      } catch (err) {
        if (err instanceof MemoryBusyError) {
          jsonError(res, 503, { error: 'service_unavailable', detail: 'memory busy; retry in a moment' });
          logRequest('POST', url, 503, Date.now() - start);
          return;
        }
        if (err instanceof SurfaceTargetNotFoundError) {
          jsonError(res, 404, { error: 'not_found', detail: 'node_id does not exist' });
          logRequest('POST', url, 404, Date.now() - start);
          return;
        }
        log(`/v1/surface/seen error: ${err}`);
        jsonError(res, 500, { error: 'internal_error' });
        logRequest('POST', url, 500, Date.now() - start);
      }
      return;
    }

    // POST /mcp — stateless MCP-over-HTTP (D-03, T-12-09)
    // Do NOT log the JSON-RPC body (T-12-05 / D-16).
    if (url === '/mcp' && req.method === 'POST') {
      const rawBody = await readBody(req);
      if (rawBody === null) {
        jsonError(res, 413, { error: 'payload_too_large' });
        logRequest('POST', url, 413, Date.now() - start);
        return;
      }
      let parsedBody: unknown;
      try { parsedBody = JSON.parse(rawBody); } catch {
        jsonError(res, 400, { error: 'bad_request', detail: 'invalid json' });
        logRequest('POST', url, 400, Date.now() - start);
        return;
      }
      // The transport handles JSON-RPC parsing, method dispatch, and the response.
      // Status/duration logging after handleRequest returns (transport writes the response).
      try {
        // WR-03: fresh McpServer + transport per request — stateless mode requires it.
        // T-12-09: sessionIdGenerator: undefined → stateless; no Mcp-Session-Id lifecycle.
        // enableJsonResponse: true → direct application/json response instead of an SSE
        // stream (more suitable for REST-style callers — Tonos, curl). The Accept header
        // must still include both application/json and text/event-stream per the SDK spec.
        const mcpServer = buildMcpServer(ops);
        const mcpTransport = new StreamableHTTPServerTransport({
          sessionIdGenerator: undefined,
          enableJsonResponse: true,
        });
        res.on('close', () => { void mcpTransport.close(); void mcpServer.close(); });
        await mcpServer.connect(mcpTransport);
        await mcpTransport.handleRequest(req, res, parsedBody);
        logRequest('POST', url, res.statusCode ?? 200, Date.now() - start);
      } catch (err) {
        log(`/mcp handleRequest error: ${err}`);
        if (!res.headersSent) {
          jsonError(res, 500, { error: 'internal_error' });
        }
        logRequest('POST', url, 500, Date.now() - start);
      }
      return;
    }

    // GET /mcp → 405 (T-12-09: stateless mode has no SSE push channel; Pitfall 4)
    if (url === '/mcp' && req.method === 'GET') {
      jsonError(res, 405, { error: 'method_not_allowed' });
      logRequest('GET', url, 405, Date.now() - start);
      return;
    }

    // ── 404 catch-all ────────────────────────────────────────────────────────
    jsonError(res, 404, { error: 'not_found' });
    logRequest(req.method ?? 'UNKNOWN', url, 404, Date.now() - start);
  };

  // ── 5. Build http.Server with timeouts (Pattern 8, D-11) ───────────────────
  const server = http.createServer((req, res) => {
    requestHandler(req, res).catch(err => {
      log(`unhandled handler error: ${err}`);
      if (!res.headersSent) {
        jsonError(res, 500, { error: 'internal_error' });
      }
    });
  });

  // T-12-07: sane timeouts for slow clients.
  // server.timeout stays at default (0 = no inactivity timeout) — MCP may hold connections.
  server.requestTimeout = 30_000;  // 30s: drop connections that don't complete a request
  server.headersTimeout = 15_000;  // 15s: drop connections that don't finish headers

  // ── 6. close() — drain server → close engine ───────────────────────────────
  // (No shared MCP transport to close — per-request transports close on res 'close', WR-03.)
  async function close(): Promise<void> {
    await new Promise<void>(r => server.close(() => r()));
    closeEngine();
  }

  return { server, close };
}

// ─── CLI entry point — fires ONLY when spawned as the main module ─────────────
if (require.main === module) {
  (async () => {
    // T-11-04 pattern: explicit DB path required — never silently open a default DB.
    const dbPath = resolveDbPath(process.argv, { fallbackToDefault: false });
    if (!dbPath) {
      // WR-02: a long-running server must fail LOUDLY on misconfiguration. Exit-0 with
      // log-file-only output is the hook convention; under systemd (Restart=on-failure)
      // it meant silent service death with nothing in journalctl.
      process.stderr.write('recense serve: no DB path supplied (--db <path> or RECENSE_DB) — exiting\n');
      log('No DB path supplied (--db <path> or RECENSE_DB env var) — exiting');
      process.exit(1);
    }

    // Generate or read token (first run: print once; subsequent runs: silent).
    const token = resolveServeToken();

    // Parse --port (default 7701) and --host (default '127.0.0.1') from argv.
    // IN-10: an invalid --port must fail loudly — `parseInt(...) || 7701` silently
    // mapped typos (`--port abc`, a missing value, `--port 0`) to the default port.
    let port = 7701;
    const portIdx = process.argv.indexOf('--port');
    if (portIdx !== -1) {
      const raw = process.argv[portIdx + 1];
      const parsed = Number(raw);
      if (!raw || !Number.isInteger(parsed) || parsed < 1 || parsed > 65535) {
        process.stderr.write(`recense serve: invalid --port value '${raw ?? ''}' — expected an integer between 1 and 65535\n`);
        process.exit(1);
      }
      port = parsed;
    }

    const hostIdx = process.argv.indexOf('--host');
    const host = hostIdx !== -1 ? (process.argv[hostIdx + 1] ?? '127.0.0.1') : '127.0.0.1';

    const { server, close } = await createBrainHttpServer({ dbPath, token });

    server.listen(port, host, () => {
      process.stdout.write(`recense serve: listening on http://${host}:${port}\n`);
    });

    // Pitfall 6: async drain before exit — never synchronous process.exit without close().
    const shutdown = () => {
      close().then(() => process.exit(0)).catch(() => process.exit(1));
    };
    process.on('SIGTERM', shutdown);
    process.on('SIGINT', shutdown);
  })().catch(err => {
    appendFileSync(LOG_PATH, `[${new Date().toISOString()}] serve-cli FATAL: ${err}\n`);
    process.exit(1);
  });
}
