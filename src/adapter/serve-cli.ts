// ---------------------------------------------------------------------------
// resolveServeToken
// ---------------------------------------------------------------------------

/**
 * Read RECENSE_SERVE_TOKEN from sleep.env (via resolveExistingEnv).
 * If present: return it silently (never print on subsequent runs — Pitfall 5).
 * If absent: generate randomBytes(32).toString('hex'), persist via writeEnvFile
 * (atomic chmod-600), and print EXACTLY ONCE three lines to stdout.
 */
export function resolveServeToken(envPath: string = sleepEnvPath()): string {
  const env = resolveExistingEnv(envPath);
  const existing = env.get('RECENSE_SERVE_TOKEN');
  if (existing) return existing; // already set — silent (Pitfall 5)

  // First run: generate, persist, print once.
  const token = randomBytes(32).toString('hex');
  const vars: Record<string, string> = {};
  for (const [k, v] of env) vars[k] = v;
  vars['RECENSE_SERVE_TOKEN'] = token;
  writeEnvFile(envPath, vars); // atomic chmod-600 write, preserves existing keys/comments

  // T-12-05: print once to stdout for the operator; never to LOG_PATH.
  // WR-06: ONLY on an interactive TTY. Under systemd, stdout IS a persistent log
  // (journald retains it indefinitely, readable by root/adm and included in journal
  // exports) — printing the token there violates the "never in logs" invariant.
  // Non-TTY runs are pointed at sleep.env, the token's canonical chmod-600 home.
  if (process.stdout.isTTY) {
    process.stdout.write('\nrecense serve: token generated.\n');
    process.stdout.write(`  RECENSE_SERVE_TOKEN=${token}\n`);
    process.stdout.write('  Record this token — it will NOT be printed again.\n\n');
  } else {
    process.stdout.write(
      `\nrecense serve: token generated and stored in ${envPath} ` +
      `(not printed — stdout is not a TTY; read it with: grep '^RECENSE_SERVE_TOKEN=' ${envPath})\n\n`,
    );
  }
  return token;
}

// ---------------------------------------------------------------------------
// checkAuth — constant-time Bearer token comparison (T-12-03/T-12-04)
// ---------------------------------------------------------------------------

/**
 * Verify the Authorization: Bearer header against the stored token.
 * Fires BEFORE any body parse (T-12-03). Returns false for:
 *   - absent or non-Bearer Authorization header
 *   - wrong token length (prevents timingSafeEqual throw — Pitfall 2)
 *   - mismatched token (timingSafeEqual constant-time compare)
 * Both absent-header and wrong-token paths return the identical 401 body (no enumeration).
 */
function checkAuth(req: http.IncomingMessage, storedToken: string): boolean {
  const header = req.headers['authorization'] ?? '';
  if (!header.startsWith('Bearer ')) return false;
  const provided = header.slice('Bearer '.length);
  // T-12-04: token length is a fixed 64-char hex (not secret); pre-check prevents the
  // Node.js ERR_CRYPTO_TIMING_SAFE_EQUAL_LENGTH throw from leaking as a 500 (Pitfall 2).
  if (provided.length !== storedToken.length) return false;
  try {
    return timingSafeEqual(Buffer.from(provided, 'utf8'), Buffer.from(storedToken, 'utf8'));
  } catch {
    return false; // belt-and-suspenders for unexpected encoding errors
  }
}

// ---------------------------------------------------------------------------
// readBody — incremental body read with 64KB cap (T-12-07)
// ---------------------------------------------------------------------------

/**
 * Accumulate the request body; resolve null if the cap is exceeded (caller sends 413).
 * NEVER trusts content-length (it is caller-controlled — Pitfall in RESEARCH.md).
 *
 * IMPORTANT: does NOT call req.destroy() on overflow — destroying the socket before
 * the handler writes the 413 response would kill the socket and the 413 would never
 * reach the client. Instead we stop accumulating and resolve null; the handler sends
 * 413 normally. The remaining oversized data is drained and discarded by Node.js after
 * res.end(), which also signals connection close (HTTP/1.1 default).
 */
function readBody(req: http.IncomingMessage): Promise<string | null> {
  return new Promise((resolve, reject) => {
    // CR-02: accumulate raw buffers and decode ONCE at the end. Decoding each chunk
    // independently (`chunk.toString('utf8')`) replaces any multibyte UTF-8 sequence
    // that straddles a TCP chunk boundary with U+FFFD — silently corrupting non-ASCII
    // content that then parses as valid JSON and gets persisted as a memory episode.
    const chunks: Buffer[] = [];
    let size = 0;
    let settled = false;
    req.on('data', (chunk: Buffer) => {
      if (settled) return; // discard data after overflow; let socket drain naturally
      size += chunk.length; // byte counting stays on raw chunk length (T-12-07)
      if (size > BODY_SIZE_LIMIT) {
        settled = true;
        resolve(null); // null → 413 from the caller
        return;
      }
      chunks.push(chunk);
    });
    req.on('end', () => {
      if (!settled) { settled = true; resolve(Buffer.concat(chunks).toString('utf8')); }
    });
    req.on('error', (err) => { if (!settled) { settled = true; reject(err); } });
  });
}

// ---------------------------------------------------------------------------
// json helpers
