const LOG_PATH = '/tmp/recense-serve.log';

/** 64KB body cap (D-11): incremental byte counting — content-length is not trusted. */
const BODY_SIZE_LIMIT = 65_536;

// ---------------------------------------------------------------------------
// Log helpers (D-16: log method, path, status, duration; never bodies, query text, or token)
// ---------------------------------------------------------------------------

/** Append a timestamped line to the log file (never stdout). */
const log = (msg: string): void =>
  appendFileSync(LOG_PATH, `[${new Date().toISOString()}] serve-cli: ${msg}\n`);

/**
 * One-line request log: stdout + LOG_PATH (D-16).
 * NEVER logs request/response bodies, query text, fact values, or the token.
 */
function logRequest(
  method: string,
  urlPath: string,
  status: number,
  durationMs: number,
  authFail?: boolean,
): void {
  const suffix = authFail ? ' auth:fail' : '';
  const line = `${method} ${urlPath} ${status} ${durationMs}ms${suffix}`;
  process.stdout.write(line + '\n');
  appendFileSync(LOG_PATH, `[${new Date().toISOString()}] ${line}\n`);
}

